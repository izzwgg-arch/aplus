/**
 * Sola Payments (formerly Cardknox) provider adapter.
 *
 * Supports two transaction surfaces:
 *
 *  1. Card Not Present  — chargeWithToken()
 *     The browser uses the iFields library to tokenize card data into a
 *     single-use token (xToken / SUT). That token is sent to our server
 *     and submitted here via the Sola Transaction API (cc:sale).
 *
 *  2. Card Present      — chargeCloudIM()
 *     A CloudIM-connected terminal processes the card on-device. We push
 *     the sale request to the CloudIM cloud API and it forwards to the reader.
 *
 * Credentials are read from environment variables — no DB IntegrationAccount
 * is required. Set the following in the server .env file:
 *
 *   SOLA_XKEY             — Transaction API key
 *   SOLA_IFIELDS_KEY      — iFields tokenization key (returned to browser)
 *   SOLA_CLOUDIM_KEY      — CloudIM API key (may equal SOLA_XKEY)
 *   SOLA_CLOUDIM_DEVICE_ID — Default terminal device ID
 *   SOLA_WEBHOOK_SECRET   — HMAC secret for webhook verification
 *
 * Sola Transaction API reference:
 *   https://docs.solapayments.com/api/transaction-api
 *
 * CloudIM Developer Guide:
 *   https://docs.solapayments.com/products/cloudim-developer-guide
 */

import { env } from "../../../config/env.js";

const SOLA_GATEWAY_URL = "https://x1.cardknox.com/gatewayjson";
const SOLA_CLOUDIM_URL = "https://api.cloudim.cardknox.com";
const SOFTWARE_NAME    = "APlus Center";
const SOFTWARE_VERSION = "1.0";

/* ─────────────────────────────────────────────────────────────────────────── */
/* Helpers                                                                      */
/* ─────────────────────────────────────────────────────────────────────────── */

function getXKey() {
  const key = env.solaXKey;
  if (!key) {
    const err = new Error("SOLA_XKEY is not configured");
    err.status = 500;
    throw err;
  }
  return key;
}

function getCloudIMKey() {
  return env.solaCloudIMKey || getXKey();
}

/**
 * POST to the Sola gateway and return the parsed response.
 * Throws if xResult is not "A" (Approved).
 */
async function callGateway(params) {
  const body = {
    xKey:             getXKey(),
    xVersion:         "4.5.9",
    xSoftwareName:    SOFTWARE_NAME,
    xSoftwareVersion: SOFTWARE_VERSION,
    ...params
  };

  const response = await fetch(SOLA_GATEWAY_URL, {
    method:  "POST",
    headers: { "Content-Type": "application/json" },
    body:    JSON.stringify(body)
  });

  const data = await response.json().catch(() => ({}));
  return data;
}

/* ─────────────────────────────────────────────────────────────────────────── */
/* 1. Connection test                                                           */
/* ─────────────────────────────────────────────────────────────────────────── */

export async function testConnection() {
  // Sola has no dedicated health endpoint; we verify the key is present and
  // the gateway is reachable by attempting a $0.00 auth-only that will reject
  // cleanly even in sandbox. Any network error surfaces immediately.
  if (!env.solaXKey) {
    const err = new Error("SOLA_XKEY is not configured");
    err.status = 400;
    throw err;
  }
  return { ok: true, message: "Sola Payments key is configured" };
}

